import React, { useState, useEffect, useCallback } from 'react';
import api from '../api';
import { motion, AnimatePresence } from 'framer-motion';
import { 
  TrendingUp, TrendingDown, Activity, BarChart3, 
  Search, ChevronRight, ArrowUpRight, ArrowDownRight, 
  RefreshCw, Zap, ShieldAlert, PieChart, Info, AlertTriangle
} from 'lucide-react';
import { 
  ResponsiveContainer, LineChart, Line, AreaChart, Area,
  XAxis, YAxis, CartesianGrid, Tooltip, ReferenceLine, 
  ComposedChart, BarChart, Bar
} from 'recharts';

import { useNavigate } from 'react-router-dom';
import { useSymbol } from '../context/SymbolContext';
import InstitutionalChart from '../InstitutionalChart';

const DM_MONO = "font-mono";
const SYNE = "font-[Syne]";

// Helper for large values
const formatCurrency = (val) => {
  return new Intl.NumberFormat('en-IN', {
    style: 'currency',
    currency: 'INR',
    maximumFractionDigits: 0
  }).format(val);
};

export default function Predictions({ portfolio }) {
  const { activeSymbol, setActiveSymbol } = useSymbol();
  const [searchInput, setSearchInput] = useState('');
  const [data, setData] = useState(null);
  const [portForecast, setPortForecast] = useState(null);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState(null);
  const navigate = useNavigate();

  const fetchPrediction = useCallback(async (sym) => {
    setLoading(true);
    setError(null);
    try {
      const res = await api.get(`/api/predict/predict/${sym}`);
      setData(res.data);
    } catch (err) {
      setError("Prediction unavailable for this symbol");
    } finally {
      setLoading(false);
    }
  }, []);

  const fetchPortfolioForecast = useCallback(async () => {
    if (!portfolio || !portfolio.positions || portfolio.positions.length === 0) return;
    try {
      const res = await api.post('/api/predict/predict/portfolio', portfolio);
      setPortForecast(res.data);
    } catch (err) {
      console.error("Portfolio forecast failed", err);
    }
  }, [portfolio]);

  useEffect(() => {
    fetchPrediction(activeSymbol);
  }, [activeSymbol, fetchPrediction]);

  useEffect(() => {
    if (portfolio) fetchPortfolioForecast();
  }, [portfolio, fetchPortfolioForecast]);

  const handleSearch = (e) => {
    e.preventDefault();
    if (searchInput) {
      setActiveSymbol(searchInput.toUpperCase());
    }
  };

  // Prepare chart data
  const chartData = data ? [
    ...data.history.map((val, i) => ({ 
      name: `T-${data.history.length - i}`, 
      price: val,
      isFuture: false 
    })),
    { name: 'TODAY', price: data.current_price, isFuture: false },
    ...['1M', '6M', '12M'].map(h => ({
      name: h,
      base: data.prediction[h].base,
      bull: data.prediction[h].bull,
      bear: data.prediction[h].bear,
      isFuture: true
    }))
  ] : [];

  return (
    <div className="min-h-screen bg-[#060810] text-white pb-20">
      
      {/* ── DISCLAIMER HEADER ────────────────────────────────────────────────── */}
      <div className="max-w-[1600px] mx-auto px-6 pt-8">
        <motion.div 
          initial={{ opacity: 0, y: -20 }}
          animate={{ opacity: 1, y: 0 }}
          className="bg-[#C9A84C]/5 border border-[#C9A84C]/20 p-4 rounded-xl flex gap-4 items-center"
        >
          <div className="bg-[#C9A84C]/10 p-2 rounded-lg text-[#C9A84C]">
            <AlertTriangle size={20} />
          </div>
          <p className="text-[10px] md:text-xs font-mono text-gray-400 leading-relaxed uppercase tracking-wider">
            <span className="text-[#C9A84C] font-bold">Disclaimer:</span> These predictions are generated by statistical models (Linear Regression, Monte Carlo Simulation, EMA Projection) using historical price data. Past performance does not guarantee future results. This is not financial advice. Always do your own research before investing.
          </p>
        </motion.div>
      </div>

      <div className="max-w-[1600px] mx-auto px-6 py-12 space-y-12">
        
        {/* ── TOP ACTION BAR ──────────────────────────────────────────────────── */}
        <div className="flex flex-col md:flex-row justify-between items-start md:items-center gap-6">
          <div className="space-y-1">
            <span className="text-[10px] font-bold text-[#C9A84C] uppercase tracking-[0.3em]">AI Forecast Engine</span>
            <h1 className={`text-5xl font-bold uppercase ${SYNE}`}>Profit <span className="text-[#C9A84C]">Predictor</span></h1>
          </div>

          <form onSubmit={handleSearch} className="relative w-full md:w-[400px]">
            <Search className="absolute left-4 top-1/2 -translate-y-1/2 text-gray-500" size={18} />
            <input 
              type="text"
              placeholder="Search Symbol (e.g. RELIANCE, TCS)"
              value={searchInput}
              onChange={(e) => setSearchInput(e.target.value)}
              className="w-full bg-white/5 border border-white/10 rounded-xl py-4 pl-12 pr-6 text-sm font-mono focus:border-[#C9A84C] outline-none transition-all"
            />
            <button type="submit" className="absolute right-2 top-1/2 -translate-y-1/2 bg-[#C9A84C] text-[#060810] px-4 py-2 rounded-lg text-[10px] font-bold uppercase transition-transform active:scale-95">
              Predict
            </button>
          </form>
        </div>

        {loading ? (
          <div className="space-y-6">
            <div className="h-64 bg-white/5 animate-pulse rounded-3xl" />
            <div className="grid grid-cols-1 md:grid-cols-3 gap-6">
              {[1,2,3].map(i => <div key={i} className="h-64 bg-white/5 animate-pulse rounded-3xl" />)}
            </div>
            <div className="h-[500px] bg-white/5 animate-pulse rounded-3xl" />
          </div>
        ) : error ? (
          <div className="bg-red-500/10 border border-red-500/20 p-12 rounded-3xl text-center space-y-4">
            <ShieldAlert size={48} className="mx-auto text-red-500" />
            <h2 className="text-xl font-bold uppercase">{error}</h2>
            <button onClick={() => setActiveSymbol('RELIANCE')} className="text-[#C9A84C] text-sm uppercase font-bold underline">Return to Default</button>
          </div>
        ) : (
          <motion.div 
            initial={{ opacity: 0 }}
            animate={{ opacity: 1 }}
            className="space-y-12"
          >
            {/* ── HERO RESULT CARD ──────────────────────────────────────────────── */}
            <div className="bg-white/[0.03] border border-[#C9A84C]/10 p-8 rounded-3xl relative overflow-hidden">
              <div className="absolute top-0 right-0 w-96 h-96 bg-[#C9A84C]/5 rounded-full blur-[100px] pointer-events-none" />
              
              <div className="flex flex-col md:flex-row justify-between items-start md:items-end gap-6 relative z-10">
                <div className="space-y-4">
                  <div className="space-y-1">
                    <div className="flex items-center gap-3">
                      <h2 className={`text-6xl font-bold uppercase ${SYNE}`}>{data.symbol}</h2>
                      <div className={`px-4 py-1.5 rounded-full text-[10px] font-bold border ${
                        data.model_agreement.includes('BULL') ? 'bg-[#00E5A0]/10 border-[#00E5A0]/20 text-[#00E5A0]' :
                        data.model_agreement.includes('BEAR') ? 'bg-[#FF3D5A]/10 border-[#FF3D5A]/20 text-[#FF3D5A]' :
                        'bg-gray-500/10 border-white/10 text-gray-400'
                      }`}>
                        {data.model_agreement}
                      </div>
                    </div>
                    <p className="text-gray-500 font-mono text-sm tracking-widest uppercase">ENSEMBLE PREDICTION FOR {data.symbol}.NS</p>
                  </div>
                  <button 
                    onClick={() => navigate('/dashboard')}
                    className="px-6 py-3 bg-[#C9A84C]/10 text-[#C9A84C] text-[10px] font-bold uppercase rounded-lg border border-[#C9A84C]/20 hover:bg-[#C9A84C] hover:text-[#060810] transition-all flex items-center gap-2"
                  >
                    View Full Chart <ChevronRight size={14} />
                  </button>
                </div>

                <div className="text-right">
                  <div className="text-gray-500 text-[10px] font-bold uppercase tracking-widest mb-1">Current Market Price</div>
                  <div className={`text-5xl font-bold ${DM_MONO}`}>₹{data.current_price.toLocaleString()}</div>
                </div>
              </div>
            </div>

            {/* ── HORIZON CARDS ────────────────────────────────────────────────── */}
            <div className="grid grid-cols-1 md:grid-cols-3 gap-6">
              {['1M', '6M', '12M'].map((h, i) => (
                <HorizonCard key={h} horizon={h} data={data.prediction[h]} index={i} />
              ))}
            </div>

            {/* ── PREDICTION CHART ─────────────────────────────────────────────── */}
            <div className="bg-white/[0.02] border border-white/5 p-8 rounded-3xl space-y-8">
              <div className="flex justify-between items-center">
                <h3 className={`text-xl font-bold uppercase ${SYNE}`}>Statistical <span className="text-[#C9A84C]">Projections</span></h3>
                <div className="flex gap-4">
                  <div className="flex items-center gap-2 text-[10px] text-gray-500">
                    <div className="w-3 h-0.5 bg-white" /> History
                  </div>
                  <div className="flex items-center gap-2 text-[10px] text-gray-500">
                    <div className="w-3 h-0.5 bg-[#C9A84C] border-dashed" /> Base Prediction
                  </div>
                </div>
              </div>

              <div className="h-[500px] w-full">
                <ResponsiveContainer width="100%" height="100%">
                  <ComposedChart data={chartData}>
                    <defs>
                      <linearGradient id="bullGrad" x1="0" y1="0" x2="0" y2="1">
                        <stop offset="5%" stopColor="#00E5A0" stopOpacity={0.1}/>
                        <stop offset="95%" stopColor="#00E5A0" stopOpacity={0}/>
                      </linearGradient>
                      <linearGradient id="bearGrad" x1="0" y1="0" x2="0" y2="1">
                        <stop offset="5%" stopColor="#FF3D5A" stopOpacity={0.1}/>
                        <stop offset="95%" stopColor="#FF3D5A" stopOpacity={0}/>
                      </linearGradient>
                    </defs>
                    <CartesianGrid strokeDasharray="3 3" stroke="#1e2333" vertical={false} />
                    <XAxis 
                      dataKey="name" 
                      stroke="#333" 
                      fontSize={10} 
                      tickLine={false} 
                      axisLine={false}
                    />
                    <YAxis 
                      stroke="#333" 
                      fontSize={10} 
                      tickLine={false} 
                      axisLine={false} 
                      tickFormatter={(v) => `₹${v}`}
                      domain={['auto', 'auto']}
                    />
                    <Tooltip 
                      contentStyle={{ backgroundColor: '#060810', border: '1px solid #1e2333', borderRadius: '12px' }}
                      itemStyle={{ fontSize: '10px', textTransform: 'uppercase', fontWeight: 'bold' }}
                    />
                    <Area type="monotone" dataKey="bull" stroke="none" fill="url(#bullGrad)" />
                    <Area type="monotone" dataKey="bear" stroke="none" fill="url(#bearGrad)" />
                    <Line type="monotone" dataKey="price" stroke="#fff" strokeWidth={2} dot={false} />
                    <Line type="monotone" dataKey="base" stroke="#C9A84C" strokeWidth={3} strokeDasharray="5 5" dot={{ r: 4, fill: '#C9A84C' }} />
                    <ReferenceLine x="TODAY" stroke="#C9A84C" strokeWidth={1} label={{ position: 'top', value: 'TODAY', fill: '#C9A84C', fontSize: 10, fontWeight: 'bold' }} />
                  </ComposedChart>
                </ResponsiveContainer>
              </div>

              {/* Signals Row */}
              <div className="grid grid-cols-2 md:grid-cols-4 gap-4 pt-8 border-t border-white/5">
                {Object.entries(data.signals).map(([key, val]) => (
                  <div key={key} className="bg-white/5 p-4 rounded-xl space-y-1">
                    <div className="text-[8px] font-bold text-gray-500 uppercase tracking-widest">{key.replace('_', ' ')}</div>
                    <div className={`text-[10px] font-bold uppercase ${
                      val.includes('UP') || val.includes('BULL') || val.includes('POSITIVE') ? 'text-[#00E5A0]' :
                      val.includes('DOWN') || val.includes('BEAR') || val.includes('NEGATIVE') ? 'text-[#FF3D5A]' : 'text-white'
                    }`}>{val}</div>
                  </div>
                ))}
              </div>
            </div>

            {/* ── PORTFOLIO FORECAST PANEL ────────────────────────────────────── */}
            <div className="bg-[#C9A84C]/5 border border-[#C9A84C]/10 p-8 rounded-3xl space-y-8 relative overflow-hidden">
               <div className="flex items-center gap-4">
                  <PieChart className="text-[#C9A84C]" size={28} />
                  <h3 className={`text-2xl font-bold uppercase ${SYNE}`}>My Portfolio <span className="text-[#C9A84C]">Forecast</span></h3>
               </div>

               {portForecast ? (
                 <div className="grid grid-cols-1 lg:grid-cols-3 gap-12 items-center">
                    <div className="space-y-6">
                      <div className="space-y-1">
                        <div className="text-[10px] font-bold text-gray-500 uppercase tracking-widest">Current Valuation</div>
                        <div className={`text-4xl font-bold ${DM_MONO}`}>₹{portForecast.current_value.toLocaleString()}</div>
                      </div>
                      
                      <div className="space-y-4">
                        {['1M', '6M', '12M'].map(h => (
                          <div key={h} className="flex justify-between items-center p-4 bg-white/5 rounded-xl border border-white/5">
                            <div className="text-[10px] font-bold text-gray-400 uppercase">{h} Target</div>
                            <div className="text-right">
                              <div className={`text-sm font-bold ${DM_MONO}`}>₹{portForecast.projections[h].toLocaleString()}</div>
                              <div className={`text-[10px] font-bold ${portForecast.profit_pct[h] >= 0 ? 'text-[#00E5A0]' : 'text-[#FF3D5A]'}`}>
                                {portForecast.profit_pct[h] >= 0 ? '+' : ''}{portForecast.profit_pct[h]}%
                              </div>
                            </div>
                          </div>
                        ))}
                      </div>
                    </div>

                    <div className="lg:col-span-2 h-[300px] w-full">
                       <ResponsiveContainer width="100%" height="100%">
                         <BarChart data={[
                           { h: 'NOW', val: portForecast.current_value },
                           { h: '1M', val: portForecast.projections['1M'] },
                           { h: '6M', val: portForecast.projections['6M'] },
                           { h: '12M', val: portForecast.projections['12M'] },
                         ]}>
                            <CartesianGrid strokeDasharray="3 3" stroke="#1e2333" vertical={false} />
                            <XAxis dataKey="h" stroke="#333" fontSize={10} axisLine={false} tickLine={false} />
                            <YAxis hide />
                            <Tooltip contentStyle={{ backgroundColor: '#060810', border: '1px solid #1e2333' }} />
                            <Bar dataKey="val" fill="#C9A84C" radius={[4, 4, 0, 0]} />
                         </BarChart>
                       </ResponsiveContainer>
                    </div>
                 </div>
               ) : (
                 <div className="text-center py-12 text-gray-500 font-mono text-xs">
                   NO ACTIVE POSITIONS FOUND FOR PORTFOLIO FORECAST.
                 </div>
               )}

               <div className="pt-8 border-t border-white/5 text-center italic text-[9px] text-gray-600 uppercase tracking-[0.2em]">
                 Predictions are based on statistical models and historical data. They are not financial advice.
               </div>
            </div>
          </motion.div>
        )}

      </div>
    </div>
  );
}

// ── Horizon Card Component ──────────────────────────────────────────────────
function HorizonCard({ horizon, data, index }) {
  const label = horizon === '1M' ? '1 MONTH' : horizon === '6M' ? '6 MONTHS' : '12 MONTHS';
  const isPos = data.base_pct >= 0;

  return (
    <motion.div 
      whileInView={{ opacity: 1, y: 0 }}
      initial={{ opacity: 0, y: 30 }}
      transition={{ delay: index * 0.1 }}
      className="bg-white/[0.03] border border-white/5 p-6 rounded-3xl hover:border-[#C9A84C]/30 transition-all group"
    >
      <div className="flex justify-between items-start mb-6">
        <div className="text-[10px] font-bold text-gray-500 uppercase tracking-widest">{label}</div>
        <div className={`flex items-center gap-1 text-[10px] font-bold ${isPos ? 'text-[#00E5A0]' : 'text-[#FF3D5A]'}`}>
          {isPos ? <ArrowUpRight size={14} /> : <ArrowDownRight size={14} />}
          {Math.abs(data.base_pct)}%
        </div>
      </div>

      <div className="space-y-1 mb-8">
        <div className="text-gray-500 text-[9px] font-bold uppercase tracking-widest">Base Target</div>
        <div className={`text-4xl font-bold ${DM_MONO}`}>₹{data.base.toLocaleString()}</div>
      </div>

      <div className="space-y-3 mb-8">
        <div className="flex justify-between text-[10px] font-bold">
          <span className="text-gray-500">🐻 BEAR CASE</span>
          <span className="text-[#FF3D5A]">₹{data.bear.toLocaleString()} ({data.bear_pct}%)</span>
        </div>
        <div className="flex justify-between text-[10px] font-bold bg-[#C9A84C]/10 p-2 rounded-lg">
          <span className="text-[#C9A84C]">⚖ BASE CASE</span>
          <span className="text-[#C9A84C]">₹{data.base.toLocaleString()} ({data.base_pct}%)</span>
        </div>
        <div className="flex justify-between text-[10px] font-bold">
          <span className="text-gray-500">🐂 BULL CASE</span>
          <span className="text-[#00E5A0]">₹{data.bull.toLocaleString()} ({data.bull_pct}%)</span>
        </div>
      </div>

      <div className="space-y-2">
        <div className="flex justify-between items-center text-[9px] font-bold uppercase tracking-widest text-gray-500">
          <span>Confidence Meter</span>
          <span className="text-[#C9A84C]">{data.confidence}%</span>
        </div>
        <div className="h-1.5 bg-white/5 rounded-full overflow-hidden">
          <motion.div 
            initial={{ width: 0 }}
            whileInView={{ width: `${data.confidence}%` }}
            transition={{ duration: 1, delay: index * 0.2 }}
            className="h-full bg-[#C9A84C] shimmer-sweep"
          />
        </div>
      </div>
    </motion.div>
  );
}
